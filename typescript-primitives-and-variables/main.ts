const fullName: string = 'Jorge Diaz Martinez'; //  declaring a string value variable
console.log('value of fullName:', fullName); // print value of fullName
console.log('typeof fullName:', typeof fullName); // print typeof fullName

const isCool: boolean = true; //  declaring a boolean value variable
console.log('value of isCool:', isCool); //  print value of isCool
console.log('typeof isCool:', typeof isCool); // print typeof isCool

const totalPets: number = 11; // declaring a number value variable
console.log('value of totalPets:', totalPets); //  print value of totalPets
console.log('typeof totalPets:', typeof totalPets); // print typeof totalPets

const empty: null = null; // declaring a null value variable
console.log('value of empty:', empty); //  print value of empty
console.log('typeof empty:', typeof empty); // print typeof empty

let nothing: undefined; // declaring a variable without assigning a value to it
console.log('value of nothing:', nothing); // print the value of nothing
console.log('typeof nothing:', typeof nothing); // print typeof nothing
